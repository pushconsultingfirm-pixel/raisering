-- All new tables use the get_my_organization_id() function
-- from the previous RLS fix to avoid circular policies

create policy "Users see own org notes" on contact_notes
  for select to authenticated using (organization_id = public.get_my_organization_id());
create policy "Users can insert notes" on contact_notes
  for insert to authenticated with check (organization_id = public.get_my_organization_id());
create policy "Users can update own notes" on contact_notes
  for update to authenticated using (created_by = auth.uid());
create policy "Users can delete own notes" on contact_notes
  for delete to authenticated using (created_by = auth.uid());

