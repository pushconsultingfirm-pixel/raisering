-- 9. AUTO-UPDATE TRIGGERS
-- ============================================================

-- Update contact_notes.updated_at on edit
create trigger contact_notes_updated_at
  before update on contact_notes
  for each row execute function update_updated_at();

-- Auto-update contacts.last_interaction_at when a new interaction is logged
create or replace function update_contact_last_interaction()
returns trigger as $$
begin
  update contacts
  set last_interaction_at = new.interaction_date,
      contact_attempts = contact_attempts + case when new.interaction_type = 'call' then 1 else 0 end
  where id = new.contact_id;
  return new;
end;
$$ language plpgsql security definer;

create trigger interaction_updates_contact
  after insert on contact_interactions
  for each row execute function update_contact_last_interaction();

-- Auto-update contacts.total_donated when a donation is recorded
create or replace function update_contact_total_donated()
returns trigger as $$
begin
  update contacts
  set total_donated = coalesce(total_donated, 0) + new.amount,
      pipeline_status = 'donated'
  where id = new.contact_id;
  return new;
end;
$$ language plpgsql security definer;

create trigger donation_updates_contact
  after insert on donations
  for each row execute function update_contact_total_donated();

-- Auto-update contacts.total_pledged when a pledge is created
create or replace function update_contact_total_pledged()
returns trigger as $$
begin
  update contacts
  set total_pledged = coalesce(total_pledged, 0) + new.amount,
      pipeline_status = 'pledged'
  where id = new.contact_id;
  return new;
end;
$$ language plpgsql security definer;

create trigger pledge_updates_contact
  after insert on pledges
  for each row execute function update_contact_total_pledged();

-- Auto-mark follow-ups as overdue
-- (Run this as a Supabase cron job or check on page load)
create or replace function mark_overdue_follow_ups()
returns void as $$
begin
  update follow_ups
  set status = 'overdue'
  where status = 'pending'
    and due_date < now();
end;
$$ language plpgsql security definer;

